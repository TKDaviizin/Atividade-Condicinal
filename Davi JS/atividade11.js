var a = true;
var b = false;

var andResult = a && b;
var orResult = a || b; 

var notA = !a;         
var notB = !b;      

console.log("Resultado de a && b:", andResult); // false
console.log("Resultado de a || b:", orResult);  // true
console.log("Resultado de !a:", notA);          // false
console.log("Resultado de !b:", notB);          // true
